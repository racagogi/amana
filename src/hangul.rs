#[cfg(test)]
mod test {
    use unicode_normalization::UnicodeNormalization;

    use crate::hangul::{
        keyborad::{dubeolsik_len, dubeolsik_to_qwerty, dubeolsik_word, filter_shifted},
        ComatibilityJamo, Jamo, Lettername, Position,
    };

    use super::keyborad::make_comatibility_jamo;

    #[test]
    fn u32_to_jamo() {
        assert_eq!(
            0x1100.try_into(),
            Ok(Jamo(Position::CHOSEONG, Lettername::KIYEOK))
        );
        assert_eq!(0x3131.try_into(), Ok(ComatibilityJamo(Lettername::KIYEOK)));
    }

    #[test]
    fn char_to_comatibilityjamo() {
        assert_eq!(''.try_into(), Ok(ComatibilityJamo(Lettername::KIYEOK)));
    }

    #[test]
    fn char_to_jamo() {
        assert_eq!(
            Jamo::try_from(ComatibilityJamo::try_from('').unwrap()),
            Ok(Jamo(Position::CHOSEONG, Lettername::KIYEOK))
        );
    }

    #[test]
    fn convert_to_dubelseik() {
        assert_eq!(dubeolsik_to_qwerty(''), 'r')
    }

    #[test]
    fn convert_to_dubelseik_word() {
        assert_eq!(dubeolsik_word(&"".to_string().nfd().to_string()), "rhr");
        assert_eq!(dubeolsik_word(&''.to_string().nfd().to_string()), "r");
        assert_eq!(dubeolsik_word(&''.to_string().nfd().to_string()), "dksg");
        assert_eq!(dubeolsik_word(&"rhr".to_string().nfd().to_string()), "rhr")
    }

    #[test]
    fn char_to_comatibilitychar() {
        assert_eq!(
            ""
                .nfd()
                .map(|x| make_comatibility_jamo(x))
                .collect::<Vec<char>>(),
            vec!['', '', '']
        )
    }

    #[test]
    fn word_len() {
        assert_eq!(dubeolsik_len(""), 3);
        assert_eq!(dubeolsik_len(""), 2);
        assert_eq!(dubeolsik_len(""), 5);
        assert_eq!(dubeolsik_len(""), 1);
    }

    #[test]
    fn filter_upper() {
        assert_eq!(filter_shifted(""), false);
        assert_eq!(filter_shifted(""), true);
        assert_eq!(filter_shifted(""), true);
    }
}
